#n Python, you can get user input using the built-in input() function. Here's a basic
# example:
name = input("Enter your name: ")   # always takes input is stored as string whetear it is int or float
print("Hello, " + name + "!")   
#The input() function takes a prompt as an argument and displays it to the user. 
#The user then enters a value and presses Enter, which is then captured by the input() function and stored in a variable.
 
# To take input from the user and convert it to an integer, you can use the int() function:
age = int(input("Enter your age: "))
print("You are " + str(age) + " years old.")    
#to take input from the user and convert it to a float, you can use the float() function:
weight = float(input("Enter your weight: "))
print("You weigh " + str(weight) + " kilograms.")


