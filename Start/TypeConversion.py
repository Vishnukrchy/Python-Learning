 #Converting a string into an integer using the int() function:
a = "123"
print("type(a) ", type(a))
b = int(a)
print("type(b) ", type(b))
print(b) # Output: 123

#Converting a string into a float using the float() function:
a = "123.45"
print("type(a) ", type(a))
b = float(a)
print("type(b) ", type(b))
print(b) # Output: 123.45

#Converting a string into a boolean using the bool() function:
a = "True"
print("type(a) ", type(a))
b = bool(a)
print("type(b) ", type(b))
print(b) # Output: True 

#Converting a string into a list using the list() function:
a = "Hello, World!"
print("type(a) ", type(a))
b = list(a)
print("type(b) ", type(b))
print(b) # Output: ['H', 'e', 'l', 'l', 'o', ',', ' ', 'W', 'o', 'r', 'l', 'd', '!']    





